    private void ajouter_montureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter_montureActionPerformed
        doClose(RET_OK);
        String refrence = txt_reference.getText();
        String taille_a = txt_taille_A.getText();
        String taille_pont = txt_taille_pont.getText();
        String taille_bras = txt_taille_bras.getText();
        String couleur=couleur_monture.getText();
        String type="Solaire";
        if(monture_optique.isSelected())type="Optique";
        String prix = prix_monture.getText();
        String contite = contite_monture.getText();
        String image= image_monture.getText();
        
        

        ob_monture monture=new ob_monture();

        monture.Ajouter(refrence,couleur, type, Integer.parseInt(taille_a), Integer.parseInt(taille_pont),Integer.parseInt(taille_bras),Integer.parseInt(prix),Integer.parseInt(contite),image);
    }//GEN-LAST:event_ajouter_montureActionPerformed

